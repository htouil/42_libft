#include "libft.h"

int	ft_atoi(const char *str)
{
	char	*s;
	int		i;
	int		sign;
	int		res;

	s = (char *)str;
	i = 0;
	sign = 1;
	res = 0;
	while (s[i] == ' ' || (s[i] >= 9 && s[i] <= 13))
		i++;
	if (s[i] == '-')
		sign = -1;
	if (s[i] == '-' || s[i] == '+')
		i++;
	while (s[i] >= '0' && s[i] <= '9')
	{
		res *= 10;
		res += s[i] - '0';
		i++;
	}
	return (res * sign);
}
